# 42577_전화번호목록.py
# -----------------------------------------------------
# ✅ 문제 설명:
# - 전화번호부에 적힌 전화번호 중에서, 한 번호가 다른 번호의 접두어인 경우가 있는지 확인하는 문제.
# - 접두어가 존재하면 False를 반환하고, 그렇지 않으면 True를 반환한다.
#
# ✅ 입력 형식:
# - phone_book: 문자열 배열 (1 ≤ 길이 ≤ 1,000,000)
# - 각 전화번호의 길이: 1 이상 20 이하
#
# ✅ 출력 형식:
# - 접두어 관계가 있으면 False, 없으면 True를 반환
#
# ✅ 입출력 예제:
# 🔹 예제 입력:
#   ["119", "97674223", "1195524421"]
# 🔹 예제 출력:
#   False
# -----------------------------------------------------

def solution(phone_book):
    # ✅ 전화번호부를 정렬하여 인접한 번호끼리만 비교 가능하게 만든다
    phone_book.sort()
    
    # ✅ 인접한 두 번호를 비교하여 접두어 관계가 있는지 확인
    for i in range(len(phone_book) - 1):
        # ✅ 현재 번호가 다음 번호의 접두어인 경우 False 반환
        if phone_book[i+1].startswith(phone_book[i]):
            return False
    
    # ✅ 접두어 관계가 없으면 True 반환
    return True

# -----------------------------------------------------
