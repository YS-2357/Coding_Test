# - 전화번호 리스트를 정렬한 후, 인접한 두 번호만 비교한다.
# - startswith() 메서드를 이용해 접두어 관계를 확인한다.
# - O(N log N) (정렬) + O(N) (비교) 이므로 효율성 통과 가능.
