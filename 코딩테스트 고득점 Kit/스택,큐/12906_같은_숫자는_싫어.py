# ✅ 문제 설명:
# - 숫자 배열에서 **연속적으로 중복되는 숫자를 제거**하고, 중복되지 않은 숫자만 남긴 배열을 반환한다.
# - 단, 숫자의 **순서를 유지해야 하며**, 연속된 같은 숫자는 하나만 남겨야 한다.

# ✅ 입력 형식:
# - 배열 arr (0 이상 9 이하의 숫자로 이루어진 배열, 1 ≤ len(arr) ≤ 1,000,000)

# ✅ 출력 형식:
# - 연속 중복을 제거한 배열을 반환

# ✅ 입출력 예제:
# 🔹 입력: [1, 1, 3, 3, 0, 1, 1]
# 🔹 출력: [1, 3, 0, 1]
# -----------------------------------------------------

def solution(arr):
    answer = []
    
    arr.append('a')  # ✅ 마지막 요소와 비교하기 위한 더미 값 추가 (배열 끝 처리)
    
    for i in range(len(arr) - 1):  # ✅ 현재 요소와 다음 요소를 비교
        if arr[i] != arr[i + 1]:   # ✅ 연속되지 않은 값만 결과에 추가
            answer.append(arr[i])
    
    return answer  # ✅ 최종 결과 반환

