# 12909_올바른_괄호.py
# -----------------------------------------------------
# ✅ 문제 설명:
# - '(' 와 ')' 로만 이루어진 문자열 `s`가 주어진다.
# - 문자열이 **올바른 괄호 문자열**인지 판별하라.
# - 올바른 괄호 문자열이란:
#   1. '('로 열고 ')'로 닫으며,
#   2. 괄호 짝이 모두 맞아야 한다.
#
# ✅ 입력 형식:
# - 문자열 `s` (길이 1 이상 100,000 이하)
#
# ✅ 출력 형식:
# - 올바른 괄호 문자열이면 `True`, 아니면 `False` 반환
#
# ✅ 입출력 예제:
# 예제 1: s = "()()"  → 출력: True
# 예제 2: s = "(())()" → 출력: True
# 예제 3: s = ")("    → 출력: False
# 예제 4: s = "(()("  → 출력: False
# -----------------------------------------------------

def solution(s):
    stack = []  # 괄호의 짝을 검사하기 위한 스택
    
    for ch in s:  # 문자열을 한 글자씩 순회
        if ch == '(':           # 열린 괄호는 스택에 추가
            stack.append('(')
        else:                   # 닫힌 괄호인 경우
            if stack:           # 스택에 열린 괄호가 있다면
                stack.pop()     # 짝을 이루고 제거
            else:               # 스택이 비어 있으면 짝이 없음
                return False    # 올바르지 않은 괄호 문자열
    
    if stack:  # 순회 후 스택이 비어있지 않으면 짝이 안 맞음
        return False

    return True  # 모든 괄호가 짝을 이뤘다면 올바른 문자열

