# 12939_최댓값과_최솟값.py
# -----------------------------------------------------
# ✅ 문제 설명:
# - 문자열 s가 주어지고, 공백으로 구분된 정수들이 있다.
# - 이 중에서 최솟값과 최댓값을 구해 "min max" 형식으로 반환한다.

# ✅ 입력 형식:
# - s: 공백으로 구분된 정수 문자열
#   예: "1 2 3 4" 또는 "-1 -2 -3 -4"

# ✅ 출력 형식:
# - "최솟값 최댓값" (문자열)

# ✅ 입출력 예제:
#   입력: "1 2 3 4"
#   출력: "1 4"
# -----------------------------------------------------

def solution(s):
    answer = ''  # 결과 문자열을 저장할 변수

    # 1. 문자열을 공백 기준으로 분리 → 숫자 문자열 리스트 생성
    nums = s.split()  # 예: "1 2 3 4" → ['1', '2', '3', '4']

    # 2. 각 요소를 정수로 변환 (리스트 컴프리헨션 사용)
    nums = [int(num) for num in nums]  # ['1', '2', '3'] → [1, 2, 3]

    # 3. 리스트를 정렬 (최소값과 최대값을 쉽게 접근하기 위해)
    nums.sort()  # 오름차순 정렬

    # 4. 첫 번째 요소(최솟값)와 마지막 요소(최댓값)를 문자열로 결합
    answer = str(nums[0]) + " " + str(nums[-1])

    # 5. 결과 문자열 반환
    return answer

# -----------------------------------------------------
# ✅ 사용된 개념 요약:
# - str.split(): 문자열을 공백 단위로 분리 → 리스트 반환
# - 리스트 컴프리헨션: ['1','2'] → [1,2]
# - 리스트 정렬: sort() 또는 min(), max()로 접근 가능
# - 문자열 결합: str()으로 형변환 후 + 연산
# - 시간 복잡도: O(n log n) (정렬), n ≤ 입력 문자열 길이
