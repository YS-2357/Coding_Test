# 120834_외계행성의_나이.py
# -----------------------------------------------------
# ✅ 문제 설명:
# - 주어진 나이(정수)를 외계행성 표기법으로 변환
# - 0~9는 각각 'a'~'j'로 대응:
#   0 -> 'a', 1 -> 'b', ..., 9 -> 'j'
# - 입력된 나이의 각 숫자를 변환하여 문자열로 반환
#
# ✅ 입력:
# - age: 정수 (1 이상 1,000 이하)
#
# ✅ 출력:
# - 변환된 문자열
#
# ✅ 예시:
# 입력: 23 → 출력: "cd" (2->'c', 3->'d')
# -----------------------------------------------------

def solution(age):
    answer = ''  # 변환된 문자열을 저장할 변수
    age = str(age)  # 숫자를 문자열로 변환 (각 자리수 순회 가능)
    for digit in age:
        # chr(ord('a') + int(digit)) → 'a'의 아스키 코드에서 숫자만큼 더해 알파벳 생성
        answer += chr(ord('a') + int(digit))
    return answer  # 최종 변환 문자열 반환

# -----------------------------------------------------
# ✅ 사용된 개념 요약:
# - **아스키 코드 변환**: ord('a')로 'a'의 아스키 값(97) 얻기 → 숫자만큼 더해 문자 생성
# - **chr()**: 숫자(아스키 코드)를 다시 문자로 변환
# - **문자열 순회**: age를 문자열로 변환 후 각 자리 처리
# -----------------------------------------------------
