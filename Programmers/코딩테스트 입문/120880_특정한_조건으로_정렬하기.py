# -----------------------------------------------------
# ✅ 문제 설명:
# - 정수 리스트 numlist와 기준 정수 n이 주어질 때,
#   numlist의 원소를 n과 **가장 가까운 순서**로 정렬합니다.
# - 거리가 같으면 더 **큰 수**를 앞에 배치합니다.
#
# ✅ 입력:
# - numlist: 길이 1~100, 중복 없는 정수 리스트
# - n: 기준 정수 (1~10,000)
#
# ✅ 출력:
# - 문제의 조건에 맞춰 정렬된 리스트
# -----------------------------------------------------

def solution(numlist, n):
    # sorted의 key에 튜플을 사용:
    # 1) abs(x-n): 기준값과의 거리(오름차순)
    # 2) -x        : 거리가 같을 땐 큰 수부터 오도록 내림차순
    return sorted(numlist, key=lambda x: (abs(x - n), -x))

