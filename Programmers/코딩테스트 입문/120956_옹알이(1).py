# ✅ 문제 설명:
# - 문자열 리스트에서, 특정 발음 가능한 단어들("aya", "ye", "woo", "ma")로만 구성된 단어 개수를 세는 문제.

# ✅ 입력 형식:
# - babbling: 문자열 리스트 (1 ≤ len(babbling) ≤ 100)
# - 각 단어 길이는 1 이상 15 이하

# ✅ 출력 형식:
# - 발음할 수 있는 단어의 개수 (정수)

# ✅ 입출력 예제:
#   입력: ["aya", "yee", "u", "maa", "wyeoo"]
#   출력: 1
# -----------------------------------------------------

def solution(babbling):
    answer = 0  # 발음 가능한 단어 개수
    cans = ["aya", "ye", "woo", "ma"]  # 발음 가능한 단어 목록
    
    # 각 단어(bab)에 대해 검사
    for bab in babbling:
        # 발음 가능한 단어(cans)를 순회하며 교체
        for can in cans:
            # bab 안에 발음 가능한 단어가 있으면 → " "로 치환 (단, 한 번만)
            bab = bab.replace(can, " ", 1)
        
        # 치환 후 남은 문자열이 공백뿐이면 발음 가능
        if not bab.strip():  # strip()으로 공백 제거 → 비어있으면 발음 가능
            answer += 1
    
    return answer

# -----------------------------------------------------
# ✅ 사용된 개념 요약:
# - 문자열 메서드:
#   - str.replace(old, new, count): 문자열 일부를 치환 (count로 횟수 제한 가능)
#   - str.strip(): 문자열 양쪽 공백 제거
# - 알고리즘:
#   - 발음 가능한 단어를 순차적으로 치환 후 남은 문자열 확인
# - 시간 복잡도: O(N × M × L)
#   - N: 입력 단어 개수
#   - M: 발음 가능한 단어 개수 (4)
#   - L: 문자열 길이 (최대 15)
