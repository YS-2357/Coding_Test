# 120876_겹치는_선분의_길이.py
# -----------------------------------------------------
# ✅ 문제 설명:
# - 3개의 선분이 주어지고, 두 개 이상 겹치는 구간의 전체 길이를 구합니다.
#   각 선분은 [start, end] 형태이며 start < end가 보장됩니다.
#
# ✅ 입력:
# - lines: 길이 3의 리스트, 각 원소는 [start, end] (−100 ≤ start < end ≤ 100)
#
# ✅ 출력:
# - 두 개 이상 겹치는 구간의 총 길이 (정수)
#
# ✅ 예시:
#   lines = [[0, 2], [-3, -1], [-2, 1]]
#   → 겹치는 구간: [−2,−1) 길이 1, [0,1) 길이 1 → 결과 2
# -----------------------------------------------------

def solution(lines):
    answer = 0

    # 1) 검사할 전체 구간의 왼쪽/오른쪽 경계 구하기
    left  = min(start for start, _   in lines)
    right = max(  end for _,   end   in lines)

    # 2) 1단위 구간 [x, x+1)마다 덮는 선분 개수를 세어,
    #    2개 이상이면 길이 1씩 더함
    for x in range(left, right + 1):
        count = sum(1 for start, end in lines if start <= x < end)
        if count >= 2:
            answer += 1

    return answer

# -----------------------------------------------------
# ✅ 사용된 개념 요약:
# - 구간 스캔(Brute-Force): 가능한 모든 단위 구간 [x, x+1)를 직접 검사
# - 리스트 내포 & sum: 각 구간마다 선분 덮임 여부를 간결하게 계산
# - 범위 결정: 전체 선분 중 최소 시작점부터 최대 끝점까지 검사

# ✅ 시간 복잡도:
# - O(L × N): L = (right − left) 최대 200, N = 3 선분 → 매우 빠름

# ✅ 기타:
# - start ≤ x < end 조건으로 반개 열린 구간을 검사해 정확한 길이 계산
# - right+1까지 순회해도 x == right 구간은 포함되는 선분이 없어 추가 없음
