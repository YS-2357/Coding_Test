# 1845_폰켓몬.py
# -----------------------------------------------------
# ✅ 문제 설명:
# - N마리 폰켓몬 중 N/2마리를 선택해야 한다.
# - 폰켓몬 종류 번호가 주어질 때, 선택할 수 있는 종류 번호 개수의 최댓값을 구해야 한다.
#
# ✅ 입력 형식:
# - nums: 폰켓몬의 종류 번호 리스트 (길이 N, 1 ≤ N ≤ 10,000, 짝수)
# - 각 숫자는 1 이상 200,000 이하
#
# ✅ 출력 형식:
# - 선택할 수 있는 폰켓몬 종류 개수의 최댓값을 반환
#
# ✅ 입출력 예제:
# 🔹 nums = [3, 1, 2, 3]
# 🔹 출력: 2
# -----------------------------------------------------

def solution(nums):
    # ✅ set을 사용하여 중복되지 않는 종류만 추출
    unique_types = set(nums)
    
    # ✅ 고를 수 있는 최대 폰켓몬 수는 N/2
    max_pick = len(nums) // 2
    
    # ✅ 종류 수와 N/2 중 작은 값을 반환
    return min(len(unique_types), max_pick)

# -----------------------------------------------------
# ✅ 코드 설명:
# - 중복 제거를 위해 set(nums)로 종류 번호의 유일한 개수를 구함.
# - 최대 고를 수 있는 마리 수는 N/2개.
# - 종류 개수와 N/2 중 더 작은 값을 반환한다.
#
# ✅ 시간 복잡도:
# - set(nums): O(N)
# - min 함수: O(1)
# - 전체 시간 복잡도는 O(N)으로 매우 빠름.
