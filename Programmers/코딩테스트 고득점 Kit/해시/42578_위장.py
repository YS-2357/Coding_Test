# ✅ 문제 설명:
# - 스파이가 입을 수 있는 옷의 조합 수를 구하는 문제.
# - 각 종류마다 하나의 아이템을 선택하거나 선택하지 않을 수 있음.
# - 단, 아무것도 입지 않는 경우는 제외해야 한다.
#
# ✅ 입력 형식:
# - clothes: 2차원 문자열 배열 (각 원소는 [옷 이름, 옷 종류])
#   - 1 ≤ len(clothes) ≤ 30
#
# ✅ 출력 형식:
# - 가능한 옷의 조합 수 (아무것도 입지 않는 경우 제외)
#
# ✅ 입출력 예제:
# 🔹 예제 입력:
#   [["yellow_hat", "headgear"], ["blue_sunglasses", "eyewear"], ["green_turban", "headgear"]]
# 🔹 예제 출력:
#   5
# -----------------------------------------------------

def solution(clothes):
    # ✅ 답을 1로 초기화 (곱셈을 위해)
    answer = 1
    # ✅ 해시맵 초기화 (종류별 옷 개수 저장)
    hash_map = {}

    # ✅ 옷 종류별로 카운트
    for item, category in clothes:
        # 종류가 없으면 1로 초기화
        if category not in hash_map:
            hash_map[category] = 1
        # 종류가 있으면 +1
        hash_map[category] += 1

    # ✅ 종류별로 (입거나 입지 않는) 선택지 수를 모두 곱함
    for key, value in hash_map.items():
        answer *= value
    
    # ✅ 아무것도 입지 않는 경우 제외
    answer -= 1

    return answer

# -----------------------------------------------------
# ✅ 코드 설명:
# - 각 종류마다 "입는다" + "입지 않는다"의 선택이 가능하므로 (종류별 옷 수 + 1)로 계산.
# - 전체 곱을 구한 뒤, 아무것도 고르지 않은 경우 1가지를 빼야 한다.
#
# ✅ 시간 복잡도:
# - clothes 순회: O(N)
# - hash_map 순회: O(K) (종류 수)
# - 전체 O(N + K)로 매우 빠름 (N ≤ 30)
#
# ✅ 2단계 오류 없음 (정상 풀이로 최종 제출 가능)
