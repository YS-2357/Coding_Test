# ✅ 문제 설명:
# - 수포자 1, 2, 3번이 각자 일정한 패턴으로 문제를 찍습니다.
# - 실제 정답 리스트(answers)가 주어졌을 때,
#   누가 가장 많이 맞혔는지를 찾아서 그 사람(들)의 번호를 반환하는 문제입니다.
# - 동점자 발생 시, **오름차순으로 모두 반환**

# ✅ 입력 형식:
# - answers: 정답 리스트 (1 ≤ 길이 ≤ 10,000, 정답은 1~5 사이 정수)

# ✅ 출력 형식:
# - 가장 많은 문제를 맞힌 사람의 번호 리스트 (1-based, 오름차순)

# ✅ 입출력 예제:
# 예제 1:
#   입력: [1,2,3,4,5]
#   출력: [1]
# 예제 2:
#   입력: [1,3,2,4,2]
#   출력: [1,2,3]

# -----------------------------------------------------

def solution(answers):
    answer = []
    first = [1, 2, 3, 4, 5]
    second = [2, 1, 2, 3, 2, 4, 2, 5]
    third = [3, 3, 1, 1, 2, 2, 4, 4, 5, 5]
    
    scores = [0, 0, 0]
    
    for i in range(len(answers)):
        if answers[i] == first[i % len(first)]:
            scores[0] += 1
        if answers[i] == second[i % len(second)]:
            scores[1] += 1
        if answers[i] == third[i % len(third)]:
            scores[2] += 1
            
    max_scores = max(scores)
    answer = [i + 1 for i in range(len(scores)) if scores[i] == max_scores]
    
    return answer

# -----------------------------------------------------
# ✅ 나의 오답 및 실수:
# ❌ `answer[i]`라고 잘못 작성해서 KeyError 발생 → `answers[i]`로 수정함
# ❌ 리스트 컴프리헨션에서 `for i in len(scores)` 라고 써서 TypeError 발생 → `range(len(scores))`로 수정
# ❌ `=` (대입) 대신 `==` (비교) 연산자 누락

# ✅ GPT가 준 힌트 요약:
# - `%` 연산으로 패턴 반복 처리 (`pattern[i % len(pattern)]`)
# - 리스트 컴프리헨션: `range(len(scores))`로 순회하면서 최대값 비교

# ✅ 사용된 개념 요약:
# - 반복 패턴 처리 (모듈로 연산 `%`)
# - enumerate 또는 range 기반 정답 비교
# - max와 리스트 컴프리헨션으로 최고 점수자 추출

