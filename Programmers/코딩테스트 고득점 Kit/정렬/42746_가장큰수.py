# 42746_가장큰수.py
# ------------------------------------------------------
# ✅ 문제 설명:
# - 주어진 0 또는 양의 정수 numbers를 이어붙여
#   만들 수 있는 가장 큰 수를 문자열 형태로 반환한다.
#
# ✅ 입력 형식:
# - numbers: 정수 배열 (1 ≤ numbers 길이 ≤ 100, 0 ≤ 각 숫자 ≤ 1000)
#
# ✅ 출력 형식:
# - 이어붙여 만든 가장 큰 수를 문자열로 반환한다.
# - 이어붙인 결과가 '000'처럼 0만 반복될 경우 "0" 하나만 출력한다.
#
# ✅ 입출력 예제:
# 🔹 입력:
# numbers = [6, 10, 2]
# 🔹 출력:
# "6210"
#
# 🔹 입력:
# numbers = [3, 30, 34, 5, 9]
# 🔹 출력:
# "9534330"
# ------------------------------------------------------

def solution(numbers):
    # ✅ 1. 모든 숫자를 문자열로 변환
    numbers = list(map(str, numbers))
    
    # ✅ 2. 문자열을 반복해 붙인 값을 기준으로 정렬
    # - x*3로 확장하여 비교 (숫자가 1000 이하이므로 최대 3자리까지 비교 가능)
    numbers.sort(key=lambda x: x*3, reverse=True)

    # ✅ 3. 정렬된 숫자들을 이어붙여 하나의 문자열로 만든다
    result = ''.join(numbers)
    
    # ✅ 4. 문자열을 int로 변환 후 다시 문자열로 변환하여
    # - "000" 같은 경우를 "0"으로 처리
    return str(int(result))

# ------------------------------------------------------
# ✅ 핵심 요약:
# - 모든 숫자를 문자열로 변환 후 정렬
# - 비교 기준: (문자열을 3번 반복하여 정렬)
# - 최종 문자열 합친 후, 정수로 변환했다가 다시 문자열 변환해 "000" 처리
