def solution(players, callings):
    answer = []
    hash = {}
    for i, p in enumerate(players):
        hash[p] = i
    
    for c in callings:
        idx = hash[c]
        prev_p = players[idx-1]
        
        players[idx-1], players[idx] = players[idx], players[idx-1]
        
        hash[c] -= 1
        hash[prev_p] += 1
    return players

