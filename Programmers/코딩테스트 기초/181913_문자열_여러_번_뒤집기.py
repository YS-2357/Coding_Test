# 181913_문자열_여러_번_뒤집기.py
# -----------------------------------------------------
# ✅ 문제 설명:
# - 주어진 문자열 my_string에서 queries에 따라 부분 문자열을 여러 번 뒤집는다.
# - 각 query는 [s, e]로 주어지며, s부터 e까지의 부분을 역순으로 바꾼다.
# - 모든 쿼리를 처리한 후 최종 문자열을 반환한다.
#
# ✅ 입력:
# - my_string: 원본 문자열
# - queries: 2차원 리스트, 각 원소는 [s, e] (0 ≤ s ≤ e < len(my_string))
#
# ✅ 출력:
# - 여러 번 뒤집기 작업을 수행한 최종 문자열
# -----------------------------------------------------

def solution(my_string, queries):
    # ✅ queries의 각 쌍 (s, e)에 대해 순차적으로 처리
    for s, e in queries:
        # temp: my_string의 s부터 e까지 슬라이싱
        temp = my_string[s:e+1]  
        # my_string 갱신:
        #   1) s 이전 문자열 + 
        #   2) temp를 뒤집은 문자열 + 
        #   3) e 이후 문자열
        my_string = my_string[:s] + temp[::-1] + my_string[e+1:]
    
    # ✅ 모든 쿼리 적용 후 최종 문자열 반환
    return my_string

# -----------------------------------------------------
# ✅ 사용된 개념 요약:
# - 문자열 슬라이싱: s:e+1 → 구간 추출
# - 문자열 역순: temp[::-1]
# - 문자열 연결: 부분들을 합쳐 새로운 문자열 생성
#
# ✅ 시간 복잡도:
# - O(Q * N) (Q: queries 개수, N: 문자열 길이)
#   → 각 쿼리마다 슬라이싱 & 문자열 생성 O(N)
