import sys  # λΉ λ¥Έ μ…λ ¥ μ²λ¦¬λ¥Ό μ„ν• sys λ¨λ“ μ‚¬μ©

# β… μ…λ ¥ μ²λ¦¬
N = int(sys.stdin.readline())  # μ •μ N μ…λ ¥ λ°›κΈ°
board = [list(map(int, sys.stdin.readline().split())) for _ in range(N)]  # N x N μ΄λ™ λΉ„μ© ν–‰λ ¬ μ €μ¥

# β… λ°©λ¬Έ μ—¬λ¶€λ¥Ό μ €μ¥ν•  λ¦¬μ¤νΈ
visited = [False] * N  

# β… μµμ† λΉ„μ© μ €μ¥ λ³€μ (μµλ€κ°’μΌλ΅ μ΄κΈ°ν™”)
min_value = float('inf')  

# β… λ°±νΈλν‚Ήμ„ μ΄μ©ν• μ™„μ „ νƒμƒ‰ (DFS κΈ°λ°)
def TSP(start, current, cost, count):
    global min_value  

    # β… λ¨λ“  λ„μ‹λ¥Ό λ°©λ¬Έν• κ²½μ° (μ¶λ°μ μΌλ΅ λμ•„μ¤λ” λΉ„μ© μ¶”κ°€)
    if count == N and board[current][start] > 0:
        min_value = min(min_value, cost + board[current][start])  # β… μµμ† λΉ„μ© κ°±μ‹ 
        return  

    # β… λ¨λ“  λ„μ‹ νƒμƒ‰
    for next_city in range(N):
        if not visited[next_city] and board[current][next_city] > 0:  # β… λ°©λ¬Έν•μ§€ μ•μ€ λ„μ‹μ΄κ³  μ΄λ™ κ°€λ¥ν•λ©΄
            visited[next_city] = True  # β… λ°©λ¬Έ μ²λ¦¬
            TSP(start, next_city, cost + board[current][next_city], count + 1)  # β… λ‹¤μ λ„μ‹λ΅ μ΄λ™
            visited[next_city] = False  # β… λ°±νΈλν‚Ή (μ›μƒ λ³µκµ¬)

# β… λ¨λ“  λ„μ‹λ¥Ό μ¶λ°μ μΌλ΅ ν•μ—¬ νƒμƒ‰ μ‹μ‘
for start_city in range(N):
    visited[start_city] = True  # β… μ¶λ° λ„μ‹ λ°©λ¬Έ μ²λ¦¬
    TSP(start_city, start_city, 0, 1)  # β… μ¶λ°μ , ν„μ¬ μ„μΉ, λΉ„μ©, λ°©λ¬Έ κ°μ
    visited[start_city] = False  # β… λ°±νΈλν‚Ή (μ¶λ° λ„μ‹ μ›μƒ λ³µκµ¬)

# β… μµμ† λΉ„μ© μ¶λ ¥
print(min_value)  


