# 백준 1699번: 제곱수의 합 (DP - 최소 제곱수 합)
# 문제 설명:
# - 주어진 정수 N을 최소 개수의 제곱수 합으로 표현하는 문제
# - 1, 4, 9, 16, ... (제곱수)를 활용하여 합을 만들 때, 최소 개수의 합을 찾는 것이 목표

import sys

# ✅ 입력 처리
N = int(sys.stdin.readline())  # 정수 N 입력

# ✅ DP 테이블 초기화 (최악의 경우 1을 N번 더하는 방식으로 초기화)
dp = [i for i in range(N + 1)]

# ✅ 점화식을 사용한 DP 테이블 채우기 (O(N√N) 방식)
for i in range(N + 1):
    divisors = int(i ** 0.5) + 1  # 1부터 √i까지 제곱수 탐색
    for j in range(1, divisors):
        dp[i] = min(dp[i], dp[i - j*j] + 1)

# ✅ 결과 출력 (N을 최소 개수의 제곱수 합으로 표현하는 경우의 수)
print(dp[N])

