# 백준 11722번: 가장 긴 감소하는 부분 수열 (다이나믹 프로그래밍 - O(N^2))
# -----------------------------------------------------
# ✅ 문제 설명:
# - 주어진 수열에서 감소하는 부분 수열을 선택하여 가장 긴 길이를 구하는 문제.
#
# ✅ 입력 형식:
# - 첫 번째 줄에 수열의 크기 N (1 ≤ N ≤ 1,000)이 주어진다.
# - 두 번째 줄에 N개의 정수가 주어진다. (1 ≤ 정수 ≤ 1,000)
#
# ✅ 출력 형식:
# - 가장 긴 감소하는 부분 수열의 길이를 출력한다.
#
# ✅ 입출력 예제:
# 🔹 예제 입력 1:
#   6
#   10 30 10 20 20 10
# 🔹 예제 출력 1:
#   3
# -----------------------------------------------------

import sys

# ✅ 입력 처리
N = int(sys.stdin.readline())
nums = list(map(int, sys.stdin.readline().split()))

# ✅ DP 배열 초기화 (모든 원소는 최소 길이 1)
dp = [1] * N

# ✅ DP 테이블 채우기 (O(N^2))
for i in range(1, N):
    for j in range(i):
        if nums[j] > nums[i]:  # 감소하는 부분 수열 조건
            dp[i] = max(dp[i], dp[j] + 1)  # 올바른 DP 업데이트 방식

# ✅ 결과 출력 (가장 긴 감소하는 부분 수열의 길이 찾기)
print(max(dp))  # dp 배열에서 최댓값을 찾아 출력

