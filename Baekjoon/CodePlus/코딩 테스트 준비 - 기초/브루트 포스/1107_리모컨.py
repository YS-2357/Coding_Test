def get_closest_channel(N, usable_buttons):
    """
    ëª©í‘œ ì±„ë„ Nê³¼ ê°€ì¥ ê°€ê¹Œìš´ ìˆ«ìë¥¼ ì°¾ì•„ì„œ ë°˜í™˜í•˜ëŠ” í•¨ìˆ˜.
    """
    min_clicks = abs(N - 100)  # + / - ë²„íŠ¼ë§Œ ì´ìš©í•  ê²½ìš°

    # ìˆ«ì ë²„íŠ¼ì´ ê³ ì¥ë‚˜ì§€ ì•Šì•˜ë‹¤ë©´, ê°€ëŠ¥í•œ ëª¨ë“  ìˆ«ì ì¡°í•©ì„ ìƒì„±í•˜ì—¬ íƒìƒ‰
    for length in range(1, len(str(N)) + 2):  # ëª©í‘œ ì±„ë„ì˜ ìë¦¿ìˆ˜ Â±1ê¹Œì§€ íƒìƒ‰
        for candidate in product(usable_buttons, repeat=length):
            candidate_number = int("".join(map(str, candidate)))
            clicks = len(str(candidate_number)) + abs(candidate_number - N)
            min_clicks = min(min_clicks, clicks)

    return min_clicks

