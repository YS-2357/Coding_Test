# 백준 1748번: 수 이어 쓰기 1
# 문제 설명:
# 1부터 N까지의 수를 연속해서 이어서 쓰면 몇 개의 숫자가 되는지 구하는 문제.
# - 예를 들어, N = 12라면 "123456789101112"이므로 총 15개의 숫자가 된다.
# - N이 최대 100,000,000까지 크므로, 문자열 연산 없이 수학적으로 해결해야 한다.

# 입력 형식:
# - 하나의 정수 N이 주어진다. (1 ≤ N ≤ 100,000,000)

# 출력 형식:
# - 1부터 N까지 이어 썼을 때 총 몇 개의 숫자가 나오는지 출력한다.

# 예제 입력 1:
# 12
# 예제 출력 1:
# 15

import sys  # 빠른 입력을 위해 사용

n = int(sys.stdin.readline())
digits = len(str(n))  # N의 자릿수 계산

sum = 0

# ✅ 1의 자리부터 N의 자릿수까지 숫자 개수를 계산
for i in range(digits):
    if i < digits - 1:
        sum += ((10 ** (i + 1)) - (10 ** i)) * (i + 1)
    else:
        sum += (n - (10 ** i) + 1) * (i + 1)

print(sum)


# -----------------------------------------------------
# ❌ 내가 쓴 코드와 틀린 점
# 1. 마지막 자리수 계산에서 `+1`이 누락됨.
#    - 기존 코드: `(n - (10 ** i)) * (i + 1)`  → 범위 계산 오류
#    - ✅ 수정: `(n - (10 ** i) + 1) * (i + 1)`로 변경하여 정확한 범위 반영

# 2. `sum` 변수 사용 오류 가능성 (sum은 내장 함수이므로 다른 변수명을 쓰는 것이 좋음)
#    - 하지만 백준 환경에서는 `sum` 변수명이 직접적인 문제는 아님.

# 3. 연산 최적화:
#    - `10**(i+1) - 10**i` 대신 `(9 * 10**i) * (i+1)` 형태로 변경 가능 (추후 최적화 가능)

