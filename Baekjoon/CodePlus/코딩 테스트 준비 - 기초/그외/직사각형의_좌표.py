# ✅ 직사각형의 세 좌표가 주어졌을 때, 나머지 하나의 좌표를 구하는 코드
# 원리: 직사각형의 네 꼭짓점은 두 개씩 짝을 이루므로
# x좌표와 y좌표를 각각 XOR 연산하면 짝이 맞는 값은 사라지고
# 홀수 번 나타난 값(= 나머지 좌표의 값)만 남게 된다.

# 예) (x1, y1), (x2, y2), (x3, y3) 가 주어졌을 때,
# 나머지 좌표 (x4, y4) = (x1 ^ x2 ^ x3, y1 ^ y2 ^ y3)

# 함수 정의
def find_missing_point(points):
    x, y = 0, 0  # 초기값 (0으로 시작해 누적 XOR)
    for px, py in points:
        x ^= px  # x좌표 누적 XOR
        y ^= py  # y좌표 누적 XOR
    return (x, y)  # 남은 값이 바로 나머지 좌표

# ✅ 입력 예시: 직사각형의 세 좌표
points = [(1, 4), (3, 4), (1, 10)]

# 함수 실행 및 결과 출력
missing_point = find_missing_point(points)

# ✅ 출력 결과: 직사각형을 이루는 마지막 점의 좌표 출력
print(f"주어진 세 점: {points}")
print(f"나머지 하나의 좌표는: {missing_point}")

# ✅ 참고: XOR 연산은 같은 값끼리 연산 시 0이 남으며,
# 여러 값에서 짝지어지는 값들은 모두 상쇄되고
# 홀수로 남은 값(= 찾는 좌표)만 최종 결과로 남는다.
