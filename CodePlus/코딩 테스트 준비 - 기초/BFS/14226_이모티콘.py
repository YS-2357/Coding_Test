        # 1ï¸âƒ£ í™”ë©´ì˜ ì´ëª¨í‹°ì½˜ì„ í´ë¦½ë³´ë“œì— ë³µì‚¬ (ì´ì „ í´ë¦½ë³´ë“œ ê°’ ë¬´ì‹œ)
        if not visited[x][x]:  # âœ… (í˜„ì¬ í™”ë©´ì˜ ì´ëª¨í‹°ì½˜ ìˆ˜, ìƒˆë¡œìš´ í´ë¦½ë³´ë“œ ê°’)
            queue.append((x, x, z + 1))
