# ✅ 제목: Convert Integer to the Sum of Two No-Zero Integers
# ✅ 문제 설명(요약):
# - 양의 정수 n이 주어진다.
# - n = a + b로 쪼갤 때, a와 b 모두 십진 표현에 0이 포함되지 않도록 하라.
# - 가능한 해 중 하나를 반환하면 된다.
#
# ✅ 입력 형식(요지):
# - n: int (2 ≤ n ≤ 10^4)
#
# ✅ 규칙 요약:
# 1) a ≥ 1, b ≥ 1
# 2) str(a), str(b)에 '0'이 없어야 함
# 3) 임의의 한 해만 반환해도 정답 처리
#
# ✅ 입출력 예시(1개):
# - n=11 → [2, 9] (2와 9 모두 0을 포함하지 않음)
#
# ✅ 정답 코드(너의 풀이; 한 줄마다 주석):
class Solution:
    def getNoZeroIntegers(self, n: int) -> List[int]:
        for i in range(1, n):                         # a 후보를 1부터 순회
            a, b = i, n - i                           # b는 n에서 a를 뺀 값
            if "0" not in str(a) and "0" not in str(b): # 두 수 모두 '0'이 없으면
                return [a, b]                         # 해답 반환

