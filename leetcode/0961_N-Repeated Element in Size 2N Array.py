# ✅ 제목: LeetCode 961. N-Repeated Element in Size 2N Array
# 🏷️ 유형: 해시 / 카운팅
#
# ✅ 문제 설명(요약):
#   - 길이가 2N인 배열 nums에서, 어떤 값 하나가 정확히 N번 반복된다.
#   - 그 반복되는 값을 반환한다.
#
# ✅ 입력 형식(요지):
#   - nums: List[int] (길이 2N)
#
# ✅ 규칙 요약:
#   - 정확히 하나의 원소만 N번 등장한다.
#   - 나머지 원소들은 각 1번씩 등장한다.
#   - N = len(nums) // 2
#
# 🧠 핵심 불변식(Invariant):
#   - Counter로 센 빈도에서, 값이 N인 원소는 정확히 1개 존재한다.
#
# ✅ 정답 코드(나의 풀이; 절대 수정 금지)

class Solution:
    def repeatedNTimes(self, nums: List[int]) -> int:
        cnt = Counter(nums)
        n = len(nums) // 2
        
        for k, v in cnt.items():
            if v == n:
                return k

# -----------------------------------------------------
# 🔍 첫 시도 결과:
