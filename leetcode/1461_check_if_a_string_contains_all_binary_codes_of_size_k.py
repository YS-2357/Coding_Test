# 1461_check_if_a_string_contains_all_binary_codes_of_size_k.py
# -----------------------------------------------------
# ✅ 제목: LeetCode 1461. Check If a String Contains All Binary Codes of Size K
# 🏷️ 유형: Sliding Window, Bit Manipulation, Rolling Hash
#
# ✅ 문제 설명(요약):
#   - 이진 문자열 s에서 길이 k인 모든 이진 코드(총 2^k개)가 부분 문자열로 한 번 이상 등장하는지 확인한다.
#   - 모두 등장하면 True, 아니면 False를 반환한다.
#
# ✅ 입력 형식(요지):
#   - s: str (이진 문자열, '0' 또는 '1')
#   - k: int (부분 문자열 길이)
#
# ✅ 규칙 요약:
#   - 길이 k의 모든 가능한 이진 문자열(0..2^k-1)이 s의 어떤 연속 구간으로 나타나야 한다.
#   - 한 번이라도 새 코드를 발견하면 카운트를 줄이고, 모든 코드를 발견하면 즉시 True를 반환한다.
#
# 🧠 핵심 불변식(Invariant):
#   - h는 현재 인덱스 i에서 끝나는 길이 k 윈도우의 이진값(0..2^k-1)을 항상 유지한다.
#   - mask = (2^k - 1)을 통해 h를 k비트로 제한하며, 윈도우가 한 칸 이동할 때 상위 비트는 자동으로 제거된다.
#   - req는 아직 발견하지 못한 코드의 개수를 나타내며, seen[h]가 처음 True가 되는 순간에만 req가 1 감소한다.
#
# ✅ 정답 코드(나의 풀이; 절대 수정 금지)

class Solution:
    def hasAllCodes(self, s: str, k: int) -> bool:                   # 길이 k의 모든 이진 코드가 s에 존재하는지 판별
        req = 1 << k                                                 # 필요한 코드 총 개수(2^k)
        seen = [False] * req                                         # 각 코드(0..2^k-1)의 등장 여부 기록
        mask = req - 1                                               # k비트만 유지하기 위한 마스크(2^k-1)
        h = 0                                                        # 현재 윈도우의 롤링 값(이진 해시)
                                                                     #
        for i, ch in enumerate(s):                                   # 문자열을 왼쪽부터 한 글자씩 순회
            h = ((h << 1) & mask) | (ord(ch) & 1)                    # h를 1비트 좌시프트 후 k비트 유지, 현재 비트(0/1) 추가
                                                                     #
            if i >= k - 1 and not seen[h]:                           # 길이 k 윈도우가 완성되고, 해당 코드가 처음 등장하면
                seen[h] = True                                       # 해당 코드를 '발견됨'으로 표시
                req -= 1                                             # 남은 미발견 코드 개수 감소
                if req == 0:                                         # 모든 코드를 발견했다면
                    return True                                      # 즉시 True 반환
                                                                     #
        return False                                                 # 끝까지 봐도 모두 못 찾았으면 False 반환

