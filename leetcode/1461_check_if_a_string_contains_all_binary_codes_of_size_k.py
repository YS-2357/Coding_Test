# 🔍 첫 시도 결과:
#   - 정답(롤링 비트마스크 기반으로 모든 k-비트 패턴의 등장 여부를 빠르게 확인).
#
# 🔧 오답 이유 및 사용한 알고리즘 개념:
#   - 오답 가능 포인트(참고):
#     - mask로 k비트 제한을 하지 않으면 윈도우 이동 시 값이 누적되어 잘못된 코드가 기록될 수 있음.
#     - i >= k-1 조건 누락 시 길이 k 미만 구간까지 코드로 처리되어 잘못 카운트될 수 있음.
#     - 이미 본 코드에도 req를 감소시키면 중복 카운트로 조기 True가 발생할 수 있음.
#   - 사용 개념:
#     - 슬라이딩 윈도우: 길이 k의 연속 구간을 한 칸씩 이동
#     - 비트 롤링 해시: k비트 정수로 윈도우 값을 유지하며 O(1) 갱신
#     - 방문 체크 배열: 코드 존재 여부를 O(1)로 확인
#
# 📚 시간·공간 복잡도:
#   - 시간: O(n) (n = len(s), 각 문자당 O(1) 갱신/체크)
#   - 공간: O(2^k) (seen 배열 크기)
#
