class Solution:
    def sumZero(self, n: int) -> List[int]:
        answer = []
        if n % 2 == 0:                           # nì´ ì§ìˆ˜ì¼ ë•Œ
            for i in range(1, n // 2 + 1):
                answer.append(i)                 # ì–‘ìˆ˜
                answer.append(-i)                # ìŒìˆ˜
            return answer
