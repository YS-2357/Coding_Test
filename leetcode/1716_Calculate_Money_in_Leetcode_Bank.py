# ✅ 제목: LeetCode 1716. Calculate Money in Leetcode Bank
# ✅ 문제 설명(요약):
#   월요일에 1달러부터 시작해 매일 1씩 증가해 저축한다.
#   각 주의 월요일은 이전 주 월요일보다 1 더 큰 금액으로 시작한다.
#   n일 동안 저축한 총액을 구한다.
#
# ✅ 입력 형식(요지):
#   - n: 정수(1 ≤ n ≤ 1000 정도)
#
# ✅ 규칙 요약:
#   - 한 주는 7일.
#   - k번째 주(0-index 기준)의 월요일 시작액은 1+k.
#   - 주 내에서 매일 1씩 증가.
#
# ✅ 정답 코드(나의 풀이; 절대 수정 금지)
class Solution:
    def totalMoney(self, n: int) -> int:
        q, r = divmod(n, 7)                 # n을 7로 나눈 몫(q: 가득 찬 주 수)과 나머지(r: 남은 일 수)
        print(q, r)                         # 디버깅 출력(제출 시 제거 필요할 수 있음)

        if q > 0:                           # 가득 찬 주가 하나 이상이면
            ans = 0                         # 누적 합계 초기화
            multi = 0                       # 주 증가량(각 주의 월요일 시작액 증가분) 카운터
            while q > 0:                    # q주를 순차 합산
                ans += 28 + 7 * multi       # 주 합: 1+2+...+7=28, 주가 바뀔 때마다 각 요일에 +multi가 더해짐 → 7*multi
                q -= 1                      # 남은 가득 찬 주 감소
                multi += 1                  # 다음 주의 시작액 증가
            ans += sum(range(1, r+1)) + r * multi  # 남은 r일: 1..r 합 + 각 일자에 주 증가량 multi가 더해짐
            return ans                       # 총합 반환
        else:                                # 가득 찬 주가 없으면
            return sum(range(1, r+1))        # 1..r의 합만 계산해 반환
# -----------------------------------------------------
# 🔍 첫 시도 결과:
#   - 정답 일치. 반복으로 주 단위 합과 나머지 일 수를 계산.
#
# 🔧 오답 이유 및 사용한 알고리즘 개념:
#   - 사용 개념: 등차수열 합, 주 단위 반복 누적.
#   - 잠재 이슈: print 디버깅 출력은 플랫폼 채점에서 오답 처리될 수 있음.
#
# 📚 시간·공간 복잡도:
#   - 시간: O(q)  (q = n//7, 주 수만큼 루프)
#   - 공간: O(1)  (상수 변수만 사용)
# -----------------------------------------------------
