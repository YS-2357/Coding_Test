#   - O(1) 공식: 가득 찬 q주의 합 = 7*q*(q+1)/2 + 21*q,
#                남은 r일 합 = r*(q+1) + r*(r-1)/2.
#   - 반복을 제거해 상수 시간에 계산 가능.
