# 1200_Minimum Absolute Difference.py
# -----------------------------------------------------
# ✅ 제목: LeetCode 1200. Minimum Absolute Difference
# 🏷️ 유형: 정렬 / 인접 원소 비교
#
# ✅ 문제 설명(요약):
#   - 정수 배열 arr이 주어질 때,
#   - 두 원소의 차이(절댓값)가 최소가 되는 모든 쌍을 찾는다.
#   - 각 쌍은 [a, b] 형태로, a < b이며 결과는 오름차순으로 정렬된다.
#
# ✅ 입력 형식(요지):
#   - arr: List[int]
#
# ✅ 규칙 요약:
#   - 최소 절댓값 차이는 정렬된 배열에서 인접한 원소들 사이에서만 발생한다.
#   - 같은 최소 차이를 가지는 모든 인접 쌍을 결과에 포함한다.
#
# 🧠 핵심 불변식(Invariant):
#   - 배열을 정렬하면 |arr[i] - arr[j]| (j > i)의 최소값은 항상 인접한 i, i+1에서 나온다.
#   - 한 번의 선형 스캔으로 최소 차이와 해당 쌍들을 모두 수집할 수 있다.
#
# ✅ 정답 코드(나의 풀이; 절대 수정 금지)

class Solution:                                                     # LeetCode 제출 형식에 맞춘 Solution 클래스 정의
    def minimumAbsDifference(self, arr: List[int]) -> List[List[int]]:  # 최소 절댓값 차이를 갖는 쌍 리스트를 반환하는 함수
        arr.sort()                                                 # 배열을 오름차순으로 정렬
        min_diff = 10 ** 7                                         # 최소 차이를 충분히 큰 값으로 초기화
        n = len(arr)                                               # 배열 길이를 저장
        ans = []                                                   # 정답 쌍들을 저장할 리스트 초기화

        for i in range(n - 1):                                     # 인접한 원소 쌍을 비교하기 위해 i를 순회
            left = arr[i]                                          # 현재 쌍의 왼쪽 원소
            right = arr[i + 1]                                     # 현재 쌍의 오른쪽 원소
            diff = right - left                                    # 두 원소의 차이 계산(정렬되어 있으므로 절댓값 불필요)
            if diff < min_diff:                                    # 더 작은 차이를 발견한 경우
                min_diff = diff                                    # 최소 차이를 갱신
                ans = []                                           # 이전 결과를 모두 초기화
                ans.append([left, right])                          # 현재 쌍을 결과에 추가
            elif diff == min_diff:                                 # 현재 차이가 기존 최소 차이와 같은 경우
                ans.append([left, right])                          # 현재 쌍을 결과에 추가
        
        return ans                                                 # 최소 차이를 갖는 모든 쌍을 반환

